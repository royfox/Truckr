<?php
// app/Controller/UsersController.php
class UsersController extends AppController {

    public $helpers = array('Html', 'Form','Text',"Time");

    public function beforeFilter() {
        parent::beforeFilter();
        $this->Auth->allow('logout');
    }

    public function login() {
        $this->set("hide_navigation", true);
        if ($this->request->is('post')) {
            if ($this->Auth->login()) {
                if(!$this->Auth->user('active')){
                    $this->Session->setFlash(__('This account has been suspended'), 'default', array(), 'auth');
                } else {
                    return $this->redirect($this->Auth->redirect());
                }
            } else {
                $this->Session->setFlash(__('Username or password is incorrect'), 'default', array(), 'auth');
            }
        }
    }

    function logout() {
        $this->redirect($this->Auth->logout());
    }

    public function index() {
        $this->User->recursive = 0;
        $this->set('users', $this->paginate());
    }

    public function view($id = null) {
        $this->User->id = $id;
        if (!$this->User->exists()) {
            throw new NotFoundException(__('Invalid user'));
        }
        $this->set('user', $this->User->read(null, $id));
        $this->set('posts', $this->User->Post->find('all',array(
             'order' => array('modified desc'),
             'conditions' => array(
                'User.id' => $id
             ),
            'contain' => array('Comment','Comment.User', 'Room.Subscriber','Room.Subscriber.User','User', 'Room')
        )));
    }

    public function handle($username){
        $user = $this->User->find('first',array(
           'conditions' => array(
               'username' => $username
        )));
        if(!$user){
            throw new NotFoundException(__('Invalid user'));
        } else {
            $this->view($user['User']['id']);
            $this->render('view');
        }
    }

    public function add() {
        $this->set("hide_navigation", true);
        if ($this->request->is('post')) {
            $this->User->create();
            if ($this->User->save($this->request->data)) {
                $this->Session->setFlash(__('The user has been saved'));
                $this->redirect(array('action' => 'index'));
            } else {
                $this->Session->setFlash(__('The user could not be saved. Please, try again.'));
            }
        }
    }

    public function edit($id = null) {
        $this->set("hide_navigation", true);
        if($id != $this->Auth->user('id')){
            $this->Session->setFlash("Not permitted!");
            $this->redirect("/");
        }
        $this->User->id = $id;
        if (!$this->User->exists()) {
            throw new NotFoundException(__('Invalid user'));
        }
        if ($this->request->is('post') || $this->request->is('put')) {
            if ($this->User->save($this->request->data)) {
                $this->Session->setFlash(__('Your details have been saved'));
                $this->redirect(array('action' => 'view', $id));
            } else {
                $this->Session->setFlash(__('Your details could not be saved. Please, try again.'));
            }
        } else {
            $this->request->data = $this->User->read(null, $id);
            unset($this->request->data['User']['password']);
        }
    }

    public function delete($id = null) {
        if (!$this->request->is('post')) {
            throw new MethodNotAllowedException();
        }
        $this->User->id = $id;
        if (!$this->User->exists()) {
            throw new NotFoundException(__('Invalid user'));
        }
        if ($this->User->delete()) {
            $this->Session->setFlash(__('User deleted'));
            $this->redirect(array('action' => 'index'));
        }
        $this->Session->setFlash(__('User was not deleted'));
        $this->redirect(array('action' => 'index'));
    }

}
